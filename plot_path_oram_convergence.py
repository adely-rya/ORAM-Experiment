import argparse
import csv

import matplotlib.pyplot as plt


def read_convergence_csv(csv_path):
    accesses = []
    random_vs_random = []
    random_vs_fixed = []

    with open(csv_path, newline="", encoding="utf-8") as csv_file:
        reader = csv.DictReader(csv_file)
        for row in reader:
            accesses.append(int(row["accesses"]))
            random_vs_random.append(float(row["random_vs_random_distance"]))
            random_vs_fixed.append(float(row["random_vs_fixed_distance"]))

    return accesses, random_vs_random, random_vs_fixed


def main():
    parser = argparse.ArgumentParser(
        description="Plot cube ORAM statistical distance convergence."
    )
    parser.add_argument(
        "csv_path",
        nargs="?",
        default="cube_test_convergence.csv",
        help="Input CSV file generated by path_oram.go.",
    )
    parser.add_argument(
        "-o",
        "--output",
        default="cube_oram_convergence.png",
        help="Output image path.",
    )
    args = parser.parse_args()

    accesses, random_vs_random, random_vs_fixed = read_convergence_csv(args.csv_path)

    plt.figure(figsize=(8, 5))
    plt.plot(accesses, random_vs_random, marker="o", label="random vs random")
    plt.plot(accesses, random_vs_fixed, marker="s", label="random vs fixed")
    plt.xscale("log")
    plt.ylim(0, 1)
    plt.xlabel("Number of accesses")
    plt.ylabel("Statistical distance")
    plt.title("Cube ORAM Statistical Distance Convergence")
    plt.legend()
    plt.grid(True, which="both", linestyle="--", linewidth=0.5, alpha=0.7)
    plt.tight_layout()
    plt.savefig(args.output, dpi=200)
    print(f"saved: {args.output}")


if __name__ == "__main__":
    main()
